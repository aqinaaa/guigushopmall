          <a class="sum-btn" href="###" target="_blank">结算</a>
        </div>
      </div>
    </div>
  </div>
</template>

<script>
import throttle from 'lodash/throttle';
import { mapGetters } from 'vuex'
export default {
  name: 'ShopCart',
  mounted() {
    // 挂载完毕之后调用一次派发请求。因为数据更新可能会多次派发请求，所以封装成一个函数
    this.getData()

  },
  methods: {
    getData() {
      this.$store.dispatch('getShopList')
    },
    isCartChecked() {
      return this.cartInfoList.every(item => { item.isChecked == 1 });
      this.getData()
    },
    async UpdateChecked(cart, event) {
      // console.log(event.target.checked);
      let isChecked = event.target.checked ? 1 : 0;
      try {
        console.log(cart.skuId)
        console.log(isChecked)
        await this.$store.dispatch('changeChecked', {
          skuId: cart.skuId,
          isChecked: isChecked,
        })
        console.log(isChecked)
        this.getData();
      } catch (error) {
        alert(error.message);
      }
    },
    // 购物车数量动态服务器修改:添加节流函数
    handler: throttle(async function (type, disNum, cart) {
      switch (type) {
        case 'add':
          disNum = 1;
          break;
        case 'minus':
          disNum = cart.skuNum > 1 ? -1 : 0;
          break;
        case 'change':
          if (isNaN(disNum) || disNum < 1) {
            disNum = 0;
          } else {
            disNum = parseInt(disNum) - cart.skuNum;
          };
          break;
      }
      // 派发action请求
      try {
        await this.$store.dispatch('addOrUpdatedShopCar', { skuId: cart.skuId, skuNum: disNum });
        this.getData()
      } catch (error) {

      }
    }, 1000),
    // 删除购物车商品
    async deleteCarList(cart) {
      try {
        // 如果成功
        await this.$store.dispatch('deleteCarList', cart.skuId)
        this.getData()
      } catch (error) {
        // 如果失败
        alert(error.message);
      }
    },
    // 删除所有选中的商品
    // ----这个函数没有可以传递的有用的参数
    async deleteAllCheckedCart() {
      try {
        await this.$store.dispatch('deleteAllCheckedCart')
        this.getData()
      } catch (error) {
        alert(error.message);
      }
    },
    // 修改全部产品的选中状态
    async updataAllchecked(event) {
      let isChecked = event.target.checked ? '1' : '0'
      //发起全选矿选中状态修改请求
      try {
        await this.$store.dispatch('updataAllischecked', isChecked)
        this.getData()
      } catch (error) {
        alert(error.message)
      }
      // if (result = 1) {
      //   this.cartList.cartInfoList.forEach((item) => {
      //     item.checked = 1
      //     this.getData()
      //   })
      // } else {
      //   this.cartList.cartInfoList.forEach((item) => {
      //     item.checked = 0
      //     this.getData()
      //   })
      // }

    }
  },
  computed: {
    ...mapGetters(['cartList']),
    cartInfoList() {
      return this.cartList.cartInfoList || []
    },
    totalPrice() {
      let sum = 0;
      this.cartInfoList.forEach(item => {
        sum += item.skuNum * item.skuPrice;
      });
      return sum;
    }
  }
}
</script>

